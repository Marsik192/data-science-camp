import random

pi = 3.141592653
count = 0
for i in range(0, 1000):
    a = random.uniform(0, 4)  # на ґітхабі умови не було, тому пишу по пам'яті. Тут програма знаходить кілкьість з 1000 точок, точки які
    if a > pi:               # входять в квадрат зі стороною 2, але не входять у вписаний  круг з радіусом 1
        count += 1
print(count)
